
public class Pyramid {

	public static void main(String[] args) {

		
		int y = 7;
		
		// outer loop for row
		for (int i = 1; i <= 5; i++) {

			// Inner loop for column
			for (int j = 1; j <= i; j++) {

				// print star
				System.out.print("* " );

			}
			// new line

			System.out.println();

		}

	}
}

